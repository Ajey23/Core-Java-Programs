//Switch case
//program print week days using switch case

class switch1
{
    public static void main(String arr[])
    {
        String day ="Mon";

        switch (day)
        {
            case "Mon":
                System.out.println("hey it is Monday");
                break;

                case "Tue":
                    System.out.println("hey it is Tuesday");
                    break;

                    case "Wed":
                        System.out.println("hey it is Wednesday");
                        break;

                        default:
                            System.out.println("Invalid day");
        }
    }
}

